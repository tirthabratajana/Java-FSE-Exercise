// DataTypeDemo.java
public class DataTypeDemo {
    public static void main(String[] args) {
        // Integer data type
        int myInt = 100;
        System.out.println("int variable: " + myInt);

        // Floating-point data type (single precision)
        float myFloat = 10.5f; // 'f' suffix is important for float literals
        System.out.println("float variable: " + myFloat);

        // Floating-point data type (double precision)
        double myDouble = 20.75;
        System.out.println("double variable: " + myDouble);

        // Character data type
        char myChar = 'A';
        System.out.println("char variable: " + myChar);

        // Boolean data type
        boolean myBoolean = true;
        System.out.println("boolean variable: " + myBoolean);
    }
}